// JSON (JavaScript Object Notation)

// 1. JSON Syntax

{
    "name": "Abdul Raheem",

    "age": 21,
   
    "gender": "male",
}


// JSON cannot contain function but JS object can

// JSON Data: It is written in key value pairs just like JS objects and double quotes are used for both key and value

{"name":"Abdul Raheem"}


// 2. JSON Array

// It is written in square[]

["Abdul Raheem","Abdul Sami","Abdul Rehman"]


// 3. Accessing JSON Data

// We can access JSON Data using dot notation
const details={
    "name":"Abdul Raheem",
    "age":21,
    "gender":"male",
}
console.log(details.name)   // Abdul Raheem
console.log(details.age)   // 21
console.log(details.gender)  // male



